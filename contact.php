<?php
include 'db.php';

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $message = $_POST['message'];

    // Insert contact form data into the database
    $stmt = $conn->prepare("INSERT INTO contact_inquiries (name, email, message) VALUES (?, ?, ?)");
    $stmt->bind_param("sss", $name, $email, $message);
    $stmt->execute();
    $stmt->close();

    // Optionally, you can send a confirmation email to the user here
    // mail($email, "Confirmation", "We received your message.");

    echo "<script>alert('Thank you for contacting us!'); window.location.href = 'contact.php';</script>";
}
?>

<!DOCTYPE html>
